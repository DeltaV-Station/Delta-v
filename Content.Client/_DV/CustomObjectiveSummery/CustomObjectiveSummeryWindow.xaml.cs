using Content.Client.UserInterface.Controls;
using Content.Shared._DV.CustomObjectiveSummery;
using Content.Shared.Mind;
using Robust.Client.AutoGenerated;
using Robust.Client.Player;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Utility;

namespace Content.Client._DV.CustomObjectiveSummery;

[GenerateTypedNameReferences]
public sealed partial class CustomObjectiveSummeryWindow : FancyWindow
{
    [Dependency] private readonly IPlayerManager _players = default!;
    [Dependency] private readonly IEntityManager _entity = default!;

    private SharedMindSystem? _mind;

    private readonly int _maxLength = 256;

    public event Action<string>? OnSubmitted;

    public CustomObjectiveSummeryWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        SubmitButton.OnPressed +=
            _ => OnSubmitted?.Invoke(Rope.Collapse(ObjectiveSummeryTextEdit.TextRope));
        ObjectiveSummeryTextEdit.OnTextChanged += _ => UpdateWordCount();

        _mind ??= _entity.System<SharedMindSystem>();

        _mind.TryGetMind(_players.LocalSession, out var mindUid, out _);

        UpdateWordCount();

        if (_entity.TryGetComponent<CustomObjectiveSummeryComponent>(mindUid, out var summery))
            ObjectiveSummeryTextEdit.TextRope = new Rope.Leaf(summery.ObjectiveSummery);

        UpdateWordCount();
    }

    private void UpdateWordCount()
    {
        // Disable the button if its over the max length.sa
        SubmitButton.Disabled = ObjectiveSummeryTextEdit.TextLength > _maxLength;
        CharacterLimitLabel.Text = ObjectiveSummeryTextEdit.TextLength + "/" + _maxLength;
    }
}
