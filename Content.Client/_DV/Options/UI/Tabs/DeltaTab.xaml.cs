using Content.Shared._DV.CCVars;
using Content.Shared._DV.Tips;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._DV.Options.UI.Tabs;

[GenerateTypedNameReferences]
public sealed partial class DeltaTab : Control
{
    [Dependency] private readonly IEntityNetworkManager _entNet = default!;

    public DeltaTab()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        Control.AddOptionCheckBox(DCCVars.NoVisionFilters, DisableFiltersCheckBox);
        Control.AddOptionCheckBox(DCCVars.DisableGlimmerShader, DisableGlimmerEffectCheckBox);
        Control.AddOptionCheckBox(DCCVars.DisableTips, DisableTipsCheckBox);

        ResetTipsButton.OnPressed += _ => OnResetTipsPressed();

        Control.Initialize();
    }

    private void OnResetTipsPressed()
    {
        _entNet.SendSystemNetworkMessage(new ResetAllSeenTipsRequest());
    }
}
